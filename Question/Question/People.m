#import "People.h"

@implementation People

- (id)init
{
    if (self = [super init]) {
        self.name = @"Default";
    }
    return self;
}

- (id)initWithName:(NSString *)name
{
    if (self = [super init]) {
        self.name = name;
    }
    return self;
}

@end


